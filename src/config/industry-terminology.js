// â”€â”€ Helper: Get all industry profiles as array grouped by category â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
export function getIndustriesByCategory() {
  const grouped = {};
  for (const profile of Object.values(INDUSTRY_PROFILES)) {
    if (!grouped[profile.category]) grouped[profile.category] = [];
    grouped[profile.category].push(profile);
  }
  return INDUSTRY_CATEGORIES.map((cat) => ({
    category: cat,
    industries: grouped[cat] || [],
  }));
}

// â”€â”€ Helper: Resolve terminology for a tenant â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
// Merges DEFAULT_TERMINOLOGY with industry profile defaults and then
// any custom overrides saved on the tenant.
export function getTerminology(tenantTerminology = {}, industryId = null) {
  const industryDefaults = industryId ? INDUSTRY_PROFILES[industryId]?.terminology || {} : {};

  const merged = {
    ...DEFAULT_TERMINOLOGY,
    ...industryDefaults,
    ...(tenantTerminology || {}),
  };

  // Return a function: term(key, fallback?)
  return function term(key, fallback) {
    return merged[key] || fallback || DEFAULT_TERMINOLOGY[key] || key;
  };
}

